public class LengthPropertyAndMethod {
	public static void main(String[] args) {
		// Property
		int nums[] = { 4, 7, 2, 6 };
		System.out.println("Property- " + nums.length);

		// Method
		String name = "Navin";
		System.out.println("Method- " + name.length());
	}
}
// Output
// Property- 4
// Method- 5
